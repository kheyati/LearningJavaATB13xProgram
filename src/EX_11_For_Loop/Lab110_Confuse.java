package EX_11_For_Loop;

public class Lab110_Confuse {
    public static void main(String[] args) {
        for (int i = 0; i <=10; i++){ // print 0 to 10 & run 11 times
            System.out.println(i);
        }
        //recommend to start from 0
        for (int i = 0; i < 10; i ++){// print 0 - 9 and run 10 time
            System.out.println(i);
        }
        for (int i = 1; i <=10; i ++){ // print 1- 10 & run 10 times
            System.out.println(i);
        }
    }
}
