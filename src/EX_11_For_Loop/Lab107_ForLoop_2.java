package EX_11_For_Loop;

public class Lab107_ForLoop_2 {
    public static void main(String[] args) {
        //long and float can be used but it is not advisable
        //int is advisable
        // cannot use with strings

        for (long l = 1l; l <10; l++){
            System.out.println(l);
        }
        for (float f = 0.0f; f<=10.5f; f++){
            System.out.println(f);
        }
    }
}
