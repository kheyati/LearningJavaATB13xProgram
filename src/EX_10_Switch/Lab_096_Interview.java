package EX_10_Switch;

public class Lab_096_Interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code){
            default:
                System.out.println("HEllooooo");
                break;
            case 'A':
                System.out.println("65");
            case 'B':
                System.out.println("66");
        }

    }
}
