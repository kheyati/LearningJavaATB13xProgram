package EX_10_Switch;

public class Lab_095_Interview {
    public static void main(String[] args) {
        int a = 11;
        switch (-1){
            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 2:
                System.out.println("11");
                break;
            case 11:
                System.out.println("a");
                break;
        }
    }
}
